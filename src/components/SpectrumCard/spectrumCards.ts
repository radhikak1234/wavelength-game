export const shuffle = (
  array: {
    right: string;
    left: string;
  }[]
) => {
  var i = array.length;
  while (i--) {
    return array.splice(Math.floor(Math.random() * (i + 1)), 1)[0];
  }
};

export function shuffleCards(
  array: {
    right: string;
    left: string;
  }[]
) {
  var copy = array.slice(0);
  return function () {
    if (copy.length < 1) {
      copy = array.slice(0);
    }
    var index = Math.floor(Math.random() * copy.length);
    var item = copy[index];
    copy.splice(index, 1);
    return item;
  };
}

